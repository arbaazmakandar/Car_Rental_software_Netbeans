    Connection connection = null;
 public void validateUser(){
            
        String username = login_analyst_uname.getText();
        String password = login_analyst_password.getText();

        try{
             Class.forName("java.sql.Driver");
             connection = DriverManager.getConnection("jdbc:mysql://localhost:3306/tavs","root","1234");
             String url =  "select NAME, USERNAME,PASSWORD from ANALYST where USERNAME ='" + username  + "' and PASSWORD = '"+password+ "'";
             Statement st = connection.createStatement();

             ResultSet set = st.executeQuery(url);
             
             if(set.next())
             {
                 AnalystLandingPage w1 = new AnalystLandingPage();
                 w1.analyst_name.setText(set.getString(1));
                 w1.setVisible(true);
                 dispose();
                 
             }else{
                 JOptionPane.showMessageDialog(this,"Invalid Credentials");
     
             }
             
             
        }catch (Exception e){
            
        }
    }
    

